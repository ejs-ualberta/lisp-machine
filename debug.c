int uintn_to_str(uint16_t * buf, word buf_sz, word num, word base){
  if (buf_sz < 2){return -1;}
  if (num == 0){
    buf[0] = '0';
    buf[1] = '\0';
    return 1;
  }

  word i = 0;
  for (; num; ++i){
    if (i >= buf_sz - 1){return -1;}
    word digit = num % base;
    digit += 48;
    if (digit >= 58){
      digit += 7;
      if (digit >= 91){
	digit += 6;
      }
    }
    buf[i] = digit;
    num /= base;
  }

  buf[i--] = '\0';
  word retval = i;
  for (int j = 0; j < i; ++j, --i){
    uint16_t tmp = buf[j];
    buf[j] = buf[i];
    buf[i] = tmp;
  }

  return retval+1;
}


void fb_print_char(uint32_t * loc, word val, uint32_t bc, uint32_t fc){
  uint16_t * chr_ptr = chars[val];

  extern uint32_t * fb_start;
  extern UINTN b_hres;

  for (word i = 0; i < char_h; ++i){
    for (word j = 0; j < char_w; ++j){
      uint32_t * ptr = (loc + i * b_hres + j);
      if (chr_ptr[i] & (1 << (char_w - j - 1))){
	*ptr = fc;
      }else{
	*ptr = bc;
      }
    }
  }
}


void fb_print_uint(uint32_t * loc, word val, word padding){
  uint16_t buf[65];
  word kerning = 4;
  word len = uintn_to_str(buf, 65, val, 16);
  for (word i = len; i < padding; ++i){
    fb_print_char(loc, '0', 0x00000000, 0x00FFFFFF);
    loc += char_w - kerning;
  }
  for (word i = 0; i < len; ++i){
    fb_print_char(loc, buf[i], 0x00000000, 0x00FFFFFF);
    loc += char_w - kerning;
  }
}


